/*
The method will get all the player info and extract only the relevant data that can be used in the player's personal page
Return: list of players objects
*/
function extractFullPlayerData(players_info) {
  return players_info.map((player_info) => {
    const { fullname, image_path, position_id, common_name, nationality,
      birthdate, birthcountry, height, weight, player_id} = player_info;
    // Default values 
    let name = "No Team"
    let id = 0
    let position = "No Positionn"
    // Replace the defualt values if the values
    // Are set in the data. Otherwise keep them 
    if(player_info.team){
      name =  player_info.team.data.name;
      id = player_info.team.data.id
    }
    if(player_info.position)
      position = player_info.position.data.name

    return {
      id: player_id,
      fullname: fullname,
      image_path: image_path,
      position_id: position_id,
      position: position,
      team: name,
      team_id: id,
      nationality: nationality,
      birthdate: birthdate,
      birthplace: birthcountry,
      common_name: common_name,
      weight: weight,
      height: height
    };
  });
}



